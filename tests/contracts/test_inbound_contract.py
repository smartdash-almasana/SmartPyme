# Test 6: Falla si no hay ningún contenido ni metadata
# ---------------------------------------------------------------------------


def test_fails_without_any_content_or_metadata():
    """Debe fallar si no hay raw_text, raw_file_ref, raw_audio_ref, raw_image_ref ni metadata."""
    with pytest.raises(ValidationError, match="al menos uno"):
        RawInboundEvent(
            channel="telegram",
            source_type="human",
            raw_content_type="otro",
            # sin raw_text, raw_file_ref, raw_audio_ref, raw_image_ref
            # metadata vacío por defecto
        )


def test_passes_with_only_metadata():
    """Debe pasar si solo hay metadata no vacía."""
    event = RawInboundEvent(
        channel="api",
        source_type="webhook",
        raw_content_type="json",
        metadata={"event_type": "payment_received", "amount": 5000},
    )
    assert event.metadata["event_type"] == "payment_received"


