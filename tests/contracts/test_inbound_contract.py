def test_all_channels_accepted():
    """Todos los canales válidos deben ser aceptados."""
    for channel in ("telegram", "whatsapp", "web", "api", "email", "cli", "otro"):
        event = RawInboundEvent(
            channel=channel,  # type: ignore[arg-type]
            source_type="human",
            raw_content_type="text",
            raw_text="Prueba de canal",
        )
        assert event.channel == channel


def test_invalid_channel_rejected():
    """Canal inválido debe ser rechazado."""
    with pytest.raises(ValidationError):
        RawInboundEvent(
            channel="signal",  # type: ignore[arg-type]
            source_type="human",
            raw_content_type="text",
            raw_text="Prueba",
        )


def test_document_content_type_requires_file_ref():
    """raw_content_type='document' también requiere raw_file_ref."""
    with pytest.raises(ValidationError, match="raw_file_ref"):
        RawInboundEvent(
            channel="email",
            source_type="human",
            raw_content_type="document",
            raw_file_name="factura.pdf",
            # raw_file_ref ausente
        )

    # Con raw_file_ref debe pasar
    event = RawInboundEvent(
        channel="email",
        source_type="human",
        raw_content_type="document",
        raw_file_ref="storage://uploads/factura.pdf",
        raw_file_name="factura.pdf",
    )
    assert event.raw_content_type == "document"
