"""
Tests — Contrato Pydantic Capa 00: RawInboundEvent
TS_000_001_CONTRATO_RAW_INBOUND_EVENT

Valida el contrato definido en app/contracts/inbound_contract.py.

Cobertura requerida:
  1. Construir RawInboundEvent con texto libre.
  2. Construir RawInboundEvent con archivo.
  3. event_id se autogenera y es UUID válido.
  4. received_at se autogenera.
  5. processing_status default es PENDING.
  6. Falla si no hay ningún contenido ni metadata.
  7. Falla si raw_content_type=text sin raw_text.
  8. Falla si raw_file_size_bytes es negativo.
"""

import uuid
from datetime import datetime, timezone

import pytest
from pydantic import ValidationError

from app.contracts.inbound_contract import RawInboundEvent


# ---------------------------------------------------------------------------
# Test 1: Construir RawInboundEvent con texto libre
# ---------------------------------------------------------------------------


def test_build_with_text():
    """RawInboundEvent con texto libre debe construirse sin errores."""
    event = RawInboundEvent(
        channel="telegram",
        source_type="human",
        raw_content_type="text",
        raw_text="No sé cuánto stock tengo. Paulita tiene un Excel.",
    )

    assert event.channel == "telegram"
    assert event.source_type == "human"
    assert event.raw_content_type == "text"
    assert event.raw_text == "No sé cuánto stock tengo. Paulita tiene un Excel."
    assert event.processing_status == "PENDING"
    assert event.raw_file_ref is None
    assert event.raw_audio_ref is None
    assert event.raw_image_ref is None


# ---------------------------------------------------------------------------
# Test 2: Construir RawInboundEvent con archivo
# ---------------------------------------------------------------------------


def test_build_with_file():
    """RawInboundEvent con archivo debe construirse sin errores."""
    event = RawInboundEvent(
        channel="telegram",
        source_type="human",
        raw_content_type="file",
        raw_file_ref="storage://uploads/stock_mayo_paulita.xlsx",
        raw_file_name="stock_mayo_paulita.xlsx",
        raw_file_type="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
        raw_file_size_bytes=24576,
    )

    assert event.raw_content_type == "file"
    assert event.raw_file_ref == "storage://uploads/stock_mayo_paulita.xlsx"
    assert event.raw_file_name == "stock_mayo_paulita.xlsx"
    assert event.raw_file_size_bytes == 24576
    assert event.raw_text is None


# ---------------------------------------------------------------------------
# Test 3: event_id se autogenera y es UUID válido
# ---------------------------------------------------------------------------


def test_event_id_autogenerated_is_valid_uuid():
    """event_id debe autogenerarse como UUID v4 válido si no se pasa."""
    event = RawInboundEvent(
        channel="whatsapp",
        source_type="human",
        raw_content_type="text",
        raw_text="Hola",
    )

    assert event.event_id != ""
    assert len(event.event_id) == 36  # UUID con guiones
    # Debe ser parseable como UUID
    parsed = uuid.UUID(event.event_id)
    assert str(parsed) == event.event_id


def test_event_id_explicit_is_preserved():
    """Si se pasa event_id explícito, debe preservarse."""
    custom_id = str(uuid.uuid4())
    event = RawInboundEvent(
        event_id=custom_id,
        channel="web",
        source_type="human",
        raw_content_type="text",
        raw_text="Mensaje con ID explícito",
    )
    assert event.event_id == custom_id


def test_two_events_have_different_event_ids():
    """Dos eventos sin event_id explícito deben tener IDs distintos."""
    e1 = RawInboundEvent(
        channel="telegram",
        source_type="human",
        raw_content_type="text",
        raw_text="Mensaje 1",
    )
    e2 = RawInboundEvent(
        channel="telegram",
        source_type="human",
        raw_content_type="text",
        raw_text="Mensaje 2",
    )
    assert e1.event_id != e2.event_id


# ---------------------------------------------------------------------------
# Test 4: received_at se autogenera
# ---------------------------------------------------------------------------


def test_received_at_autogenerated():
    """received_at debe autogenerarse como datetime UTC si no se pasa."""
    before = datetime.now(timezone.utc)
    event = RawInboundEvent(
        channel="api",
        source_type="system",
        raw_content_type="json",
        metadata={"source": "webhook_test"},
    )
    after = datetime.now(timezone.utc)

    assert event.received_at is not None
    assert isinstance(event.received_at, datetime)
    assert before <= event.received_at <= after


